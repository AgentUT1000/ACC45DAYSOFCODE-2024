#include <bits/stdc++.h>
using namespace std;

int main() {
    int T;
    cin >> T;
    
    while (T--) {
        int A, B, C;
        cin >> A >> B >> C;
        
        int total = A + B + C;
        int disco = min({A, B, C});
        int final = total - disco;
        
        cout << final<< endl;
    }
}
